def test_always_visible_tools_appear_in_listing() -> None:
    """Under bm25, ``list_tools()`` includes the five pinned tools, search synthetics, and resource synthetics."""
    env = {"GCO_MCP_TOOL_SEARCH": "bm25"}
    for var in ("GCO_ENABLE_ALL_TOOLS", "GCO_ENABLE_DESTRUCTIVE_OPERATIONS"):
        os.environ.pop(var, None)
    with patch.dict(os.environ, env, clear=False):
        mcp_instance = _reload_mcp_with_env(env)
        names = set(_list_tool_names_via_public_api(mcp_instance))

    expected_pinned = {
        "find_examples",
        "find_docs",
        "list_jobs",
        "submit_job_sqs",
        "list_inference_endpoints",
    }
    expected_search_synthetics = {"search_tools", "call_tool"}
    expected_resource_synthetics = {"list_resources", "read_resource"}

    missing = (expected_pinned | expected_search_synthetics | expected_resource_synthetics) - names
    assert not missing, f"expected pinned/synthetic tools missing from listing: {missing!r}"


def test_default_env_uses_bm25() -> None:
    """With no ``GCO_MCP_TOOL_SEARCH`` set, the default is BM25 — the listing is the search-mode shape."""
    for var in (
        "GCO_MCP_TOOL_SEARCH",
        "GCO_ENABLE_ALL_TOOLS",
        "GCO_ENABLE_DESTRUCTIVE_OPERATIONS",
    ):
        os.environ.pop(var, None)
    mcp_instance = _reload_mcp_with_env({})
    names = set(_list_tool_names_via_public_api(mcp_instance))

    # BM25 mode produces a small, fixed-shape listing (≤ ~10 entries):
    # five pinned tools + search_tools/call_tool + list_resources/read_resource.
    # The full catalog under "off" mode runs into the 90s — assert against
    # both the synthetic-tools presence and the absence of representative
    # full-catalog entries to confirm BM25 is what's wired.
    assert "search_tools" in names, "expected BM25 search synthetic in default-env listing"
    assert "call_tool" in names, "expected BM25 call_tool synthetic in default-env listing"
    # Tools that aren't on the always-visible list shouldn't appear.
    assert "list_stacks" not in names, (
        "default-env listing should be BM25-mode (catalog-replaced), not legacy full catalog"
    )
    assert "cost_summary" not in names, (
        "default-env listing should be BM25-mode (catalog-replaced), not legacy full catalog"
    )


def test_search_off_returns_full_catalog() -> None:
    """Under ``GCO_MCP_TOOL_SEARCH=off``, the listing is the full catalog with no synthetics."""
    env = {"GCO_MCP_TOOL_SEARCH": "off"}
    with patch.dict(os.environ, env, clear=False):
        mcp_instance = _reload_mcp_with_env(env)
        names = set(_list_tool_names_via_public_api(mcp_instance))

    # No search synthetics in off-mode.
    assert "search_tools" not in names, "off-mode should not register the BM25 search synthetic"
    assert "call_tool" not in names, "off-mode should not register the BM25 call_tool synthetic"
    # Real tools are visible — pick representatives from each of the major domains.
    for real_tool in (
        "list_jobs",
        "list_stacks",
        "cost_summary",
        "list_inference_endpoints",
        "list_models",
    ):
        assert real_tool in names, f"off-mode should expose the full catalog; missing {real_tool!r}"
    # Resource-as-tools synthetics still register because the transform sits
    # below the catalog-replacement layer.
    assert "list_resources" in names
    assert "read_resource" in names


def test_unknown_tool_search_value_falls_back_to_bm25() -> None:
    """An unrecognised value for ``GCO_MCP_TOOL_SEARCH`` (e.g. ``banana``) falls back to BM25."""
    env_unknown = {"GCO_MCP_TOOL_SEARCH": "banana"}
    env_bm25 = {"GCO_MCP_TOOL_SEARCH": "bm25"}
    for var in ("GCO_ENABLE_ALL_TOOLS", "GCO_ENABLE_DESTRUCTIVE_OPERATIONS"):
        os.environ.pop(var, None)

    with patch.dict(os.environ, env_unknown, clear=False):
        mcp_unknown = _reload_mcp_with_env(env_unknown)
        names_unknown = set(_list_tool_names_via_public_api(mcp_unknown))

    with patch.dict(os.environ, env_bm25, clear=False):
        mcp_bm25 = _reload_mcp_with_env(env_bm25)
        names_bm25 = set(_list_tool_names_via_public_api(mcp_bm25))

    assert names_unknown == names_bm25, (
        f"unknown tool-search value should fall back to bm25 listing; "
        f"diff: {names_unknown ^ names_bm25!r}"
    )


