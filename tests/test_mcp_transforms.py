"""
Tests for the GCO MCP server's FastMCP transforms (mcp/server.py).

Covers the always-on Resources As Tools transform — the tool-only
clients (Cursor, etc.) reach resources through synthetic
``list_resources`` and ``read_resource`` tools generated by the
transform. The round-trip test confirms a resource registered against
a FastMCP instance with that transform is reachable through the
synthetic tool surface and returns the same payload the resource
handler does directly.

Also covers search-mode selection (BM25 default, regex, code_mode,
off, fallback for unknown values), Code Mode discovery-tool wiring,
audit-log experimental marker, and sandbox-limit env-var tunability.
"""

import asyncio
import importlib
import os
import sys
from pathlib import Path
from unittest.mock import patch

import pytest
from fastmcp import FastMCP
from fastmcp.server.transforms import ResourcesAsTools

# Ensure mcp/ is importable, mirroring tests/test_mcp_feature_flags.py.
sys.path.insert(0, str(Path(__file__).parent.parent / "mcp"))


@pytest.mark.asyncio
async def test_resources_as_tools_round_trip() -> None:
    """Synthetic ``read_resource`` returns the resource handler's payload.

    The Resources As Tools transform exposes a ``read_resource`` synthetic
    tool that proxies any registered resource. This test builds a fresh
    FastMCP instance with only that transform attached, registers a
    minimal resource, invokes the synthetic tool through ``call_tool``,
    and asserts the text payload matches the handler's direct return.

    Using a fresh instance (rather than the production ``mcp`` singleton
    from ``mcp/server.py``) keeps this test hermetic: nothing it
    registers leaks into other tests that count tools or resources on
    the live server.
    """
    test_mcp = FastMCP("transforms-roundtrip-test")
    test_mcp.add_transform(ResourcesAsTools(test_mcp))

    expected = "hello round trip"

    @test_mcp.resource("test://gco/round-trip")
    async def _round_trip_resource() -> str:
        return expected

    # Sanity-check the handler returns what we expect when called directly.
    assert await _round_trip_resource() == expected

    result = await test_mcp.call_tool("read_resource", {"uri": "test://gco/round-trip"})

    # FastMCP's ToolResult exposes both ``content`` (a list of TextContent
    # items) and ``structured_content`` (a dict). Assert both shapes match
    # so a future change in either surface is caught by this test.
    assert result.content, "read_resource returned no content blocks"
    assert result.content[0].text == expected
    assert result.structured_content == {"result": expected}


# =============================================================================
# Tool Search filtering tests
# =============================================================================
#
# These tests reload ``mcp/server.py`` under different ``GCO_MCP_TOOL_SEARCH``
# values and assert the resulting ``list_tools()`` matches the configured
# transform mode. Reloading the server module is what triggers the transform
# wiring at import time, which is why every test patches ``os.environ`` first
# and then calls ``importlib.reload(server)`` (and ``run_mcp`` so the live
# tools and resources re-register against the freshly built FastMCP instance).


def _reload_mcp_with_env(env: dict[str, str]) -> object:
    """Reload ``mcp.server`` and every ``mcp.tools.*`` / ``mcp.resources.*`` module.

    Reloading ``server`` is what re-runs the transform-wiring block (which
    reads ``GCO_MCP_TOOL_SEARCH`` from env at module-import time). Reloading
    every tool and resource module afterward forces their ``@mcp.tool`` /
    ``@mcp.resource`` decorators to re-bind to the freshly built FastMCP
    instance — without that, the new instance is empty and the listing
    only contains the synthetic transform tools.

    Returns the live ``mcp`` FastMCP instance attached to ``run_mcp.mcp``
    after the reload. Caller is responsible for restoring the env via the
    surrounding ``patch.dict`` context manager.
    """
    import run_mcp
    import server

    # Drop cached tools.* and resources.* modules so the freshly reloaded
    # ``server`` builds a clean FastMCP and every decorator re-fires.
    cached_submodules = [
        name
        for name in list(sys.modules)
        if name.startswith("tools.")
        or name.startswith("resources.")
        or name in ("tools", "resources")
    ]
    for name in cached_submodules:
        sys.modules.pop(name, None)

    importlib.reload(server)
    importlib.reload(run_mcp)
    return run_mcp.mcp


def _list_tool_names_via_public_api(mcp_instance: object) -> list[str]:
    """Return the names a client would see via ``mcp.list_tools()``.

    Uses the public coroutine (which runs through the transform pipeline)
    rather than ``_list_tools()`` (which bypasses transforms). The public
    surface is what the search-mode behaviour tests care about.
    """
    tools = asyncio.run(mcp_instance.list_tools())
    return [t.name for t in tools]


