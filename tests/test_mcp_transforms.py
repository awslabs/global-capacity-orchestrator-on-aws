def test_code_mode_replaces_catalog() -> None:
    """Under ``code_mode``, ``list_tools()`` exposes only the Code Mode meta-tools + resource synthetics."""
    env = {"GCO_MCP_TOOL_SEARCH": "code_mode"}
    with patch.dict(os.environ, env, clear=False):
        mcp_instance = _reload_mcp_with_env(env)
        names = set(_list_tool_names_via_public_api(mcp_instance))

    # Code Mode discovery + execute meta-tools must all be present. The
    # default tool names from FastMCP's experimental Code Mode classes are
    # ``tags`` (GetTags), ``search`` (Search), ``get_schema`` (GetSchemas),
    # and ``execute`` (CodeMode itself).
    for meta in ("search", "get_schema", "execute"):
        assert meta in names, f"expected Code Mode meta-tool {meta!r} in listing"

    # ResourcesAsTools sits below the catalog-replacement layer so its
    # synthetic tools survive the Code Mode replacement.
    assert "list_resources" in names
    assert "read_resource" in names

    # Original tool names must NOT appear directly — Code Mode replaced the
    # catalog with the meta-tool surface.
    for original in ("list_jobs", "find_examples", "cost_summary", "list_models"):
        assert original not in names, (
            f"expected original tool {original!r} to be replaced by Code Mode meta-tools"
        )


def test_code_mode_uses_four_stage_discovery() -> None:
    """Code Mode is constructed with ``[GetTags, Search, GetSchemas]`` discovery factories."""
    from fastmcp.experimental.transforms.code_mode import (
        CodeMode,
        GetSchemas,
        GetTags,
        Search,
    )

    env = {"GCO_MCP_TOOL_SEARCH": "code_mode"}
    with patch.dict(os.environ, env, clear=False):
        mcp_instance = _reload_mcp_with_env(env)
        attached = [t for t in mcp_instance.transforms if isinstance(t, CodeMode)]

    assert len(attached) == 1, (
        f"expected exactly one CodeMode transform attached, got {len(attached)}"
    )
    factories = list(attached[0]._discovery_factories)
    assert len(factories) == 3, f"expected 3 discovery factories, got {len(factories)}"
    assert isinstance(factories[0], GetTags), (
        f"first discovery factory should be GetTags, got {type(factories[0]).__name__}"
    )
    assert isinstance(factories[1], Search), (
        f"second discovery factory should be Search, got {type(factories[1]).__name__}"
    )
    assert isinstance(factories[2], GetSchemas), (
        f"third discovery factory should be GetSchemas, got {type(factories[2]).__name__}"
    )


def test_code_mode_audit_log_marks_experimental(caplog: pytest.LogCaptureFixture) -> None:
    """Under ``code_mode``, the startup audit entry has both ``tool_search`` and ``code_mode_experimental``."""
    import json
    import logging as _logging

    import audit

    env = {"GCO_MCP_TOOL_SEARCH": "code_mode"}
    with (
        patch.dict(os.environ, env, clear=False),
        caplog.at_level(_logging.INFO, logger="gco.mcp.audit"),
    ):
        # ``_resolve_tool_search`` reads ``GCO_MCP_TOOL_SEARCH`` from env on
        # every call, so no reload is needed — the patched env is enough.
        audit.emit_startup_log()

    startup_entries = [
        json.loads(rec.getMessage())
        for rec in caplog.records
        if rec.name == "gco.mcp.audit" and rec.getMessage().startswith("{")
    ]
    startup_entries = [e for e in startup_entries if e.get("event") == "mcp.server.startup"]
    assert startup_entries, "expected at least one mcp.server.startup audit entry"
    entry = startup_entries[-1]
    assert entry.get("tool_search") == "code_mode", (
        f"startup entry should record tool_search='code_mode', got {entry.get('tool_search')!r}"
    )
    assert entry.get("code_mode_experimental") is True, (
        f"startup entry should mark code_mode_experimental=True, got "
        f"{entry.get('code_mode_experimental')!r}"
    )


